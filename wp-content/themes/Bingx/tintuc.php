                  <p>As a rule, the Group&#8217;s revenues are positively affected by higher activity of financial
                    markets due to the fact that in such periods, a higher level of turnover is realized by the
                    Group&#8217;s clients and higher profitability per lot. The periods of clear and long market trends
                    are favourable for the Company, and it is at such times that it achieves the highest revenues.
                    Therefore, high activity of financial markets and commodities generally leads to an increased volume
                    of trading on the Group&#8217;s trading platforms. On the other hand, the decrease in this activity
                    and the related decrease in the transaction activity of the Group&#8217;s clients leads, as a rule,
                    to a decrease in the Group&#8217;s operating income. Due to the above, operating income and the
                    Group&#8217;s profitability may decrease in periods of low activity of financial and commodity
                    markets. In addition, there may be a more predictable trend in which the market moves within a
                    limited price range. This leads to market trends that can be predicted with a higher probability
                    than in the case of larger directional movements on the markets, which creates favourable conditions
                    for transactions concluded in a narrow range trading. In this case, a greater number of transactions
                    that bring profits to clients is observed, which leads to a decrease in the Group&#8217;s result on
                    market making. </p>
